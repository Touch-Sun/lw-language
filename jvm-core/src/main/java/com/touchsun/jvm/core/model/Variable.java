package com.touchsun.jvm.core.model;

/**
 * 变量对象
 * 
 * @author lee
 */
public class Variable {
    
    private final int id;
    private final int type;
    private final String value;

    public Variable(int id, int type, String value) {
        this.id = id;
        this.type = type;
        this.value = value;
    }

    public int getType() {
        return type;
    }

    public String getValue() {
        return value;
    }

    public int getId() {
        return id;
    }
}
